    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat sdf = new SimpleDateFormat ("yyyy-MM-dd");
        String tanggal = sdf.format(tgl.getDate ());
        String sql = "insert into data_pemesanan values (?,?,?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setInt(1,Integer.parseInt (tid.getText()));
            stat.setString(2, tnamacos.getText());
            stat.setString(3, tnamapt.getText());
            stat.setString(4, talamatpt.getText());
            stat.setString(5, tno.getText());
            stat.setString(6, tkodebarang.getText());
            stat.setString(7, tnamabrg.getText());
            stat.setInt(8,Integer.parseInt (tharga.getText()));
            stat.setInt(9,Integer.parseInt (tjumlah.getText()));
            stat.setString(10, tanggal);
           
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
            kosong();
            tid.requestFocus();
            datatable();
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan" +e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

