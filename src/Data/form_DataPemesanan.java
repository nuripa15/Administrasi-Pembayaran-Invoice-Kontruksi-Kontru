    protected void datatable(){
        Object[] baris ={"ID Costumer","Nama Costumer","Perusahaan","Alamat Perusahaan","No Telepon","Kode Barang","Nama Barang","Harga Barang","Jumlah Barang","Tanggal Pesan"};
        tabmode = new DefaultTableModel(null, baris);
        tabelpemesanan.setModel(tabmode);
        String sql = "select * from data_pemesanan";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("ID");
                String b = hasil.getString("Nama");
                String c = hasil.getString("Perusahaan");
                String d = hasil.getString("Alamat");
                String e = hasil.getString("NoTelp");
                String f = hasil.getString("kode_barang");
                String g = hasil.getString("nama_barang");
                String h = hasil.getString("harga_barang");
                String i = hasil.getString("jumlah_barang");
                String j = hasil.getString("tanggal");
                
                String[] data={a,b,c,d,e,f,g,h,i,j};
                tabmode.addRow(data);
            }     
        }catch (Exception e){
        }
        }

